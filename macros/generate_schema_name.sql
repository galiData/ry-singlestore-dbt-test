-- Override default schema naming so custom schemas don't get prefixed
-- with the target schema. E.g., marts/core stays "core", not "dev_core".

{% macro generate_schema_name(custom_schema_name, node) -%}

    {%- set default_schema = target.schema -%}

    {%- if custom_schema_name is none -%}
        {{ default_schema }}
    {%- else -%}
        {{ custom_schema_name | trim }}
    {%- endif -%}

{%- endmacro %}
